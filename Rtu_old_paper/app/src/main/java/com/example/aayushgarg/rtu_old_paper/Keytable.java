public class Keytable {

private String key;
private String value;

    public Keytable() {
    }

    public Keytable(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }
}
